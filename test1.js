//Multiple if-else statements
let number = 15;
if (number > 0) {
  console.log("The number is positive.");
}
if (number % 3 === 0) {
  console.log("The number is divisible by 3.");
}
if (number < 100) {
  console.log("The number is less than 100.");
}


//switch statements
